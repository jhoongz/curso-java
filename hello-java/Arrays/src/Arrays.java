public class Arrays {

    public static void main(String[] args) {

        int[] n = new int[4];
        System.out.println(n);
        n[0] = 7;
        n[1] = 17;
        n[2] = 24;
        System.out.println(n[0]);
        System.out.println(n[1]);
        System.out.println(n[2]);
        System.out.println(n[3]);
        n[3] = 31;
        System.out.println(n[3]);

    }
}