public class Auto {
    private int anio;
    private String color;
    private int puertas;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getPuertas() {
        return puertas;
    }

    public void setPuertas(int puertas) {
        this.puertas = puertas;
    }

    public void setAnio (int anio) {
        this.anio = anio;
    }

    public int getAnio () {
        return this.anio;
    }
}
